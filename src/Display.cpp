// 'Normal', 128x64px
void DisplaySetup()
{
	// SSD1306_SWITCHCAPVCC = generate display voltage from 3.3V internally
	if (!oled.begin(SSD1306_SWITCHCAPVCC, 0x3C))
	{
		//Serial.println(F("SSD1306 allocation failed"));
		weHaveOled = false;
		return;
	}
	else
	{
		weHaveOled = true;
		Serial.println(F("SSD1306 found."));
	}
	oled.clearDisplay();
	oled.setTextSize(1);	  // Normal 1:1 pixel scale
	oled.setTextColor(WHITE); // Draw white text
	oled.drawBitmap(0, 0, epd_bitmap_stuck, 128, 64, 1);
	oled.display();
}
long lastBatteryLevelUpdate = 10000;
bool showingBatteryLevel = true;
void DisplayLoop()
{
	if (showingBatteryLevel){ // battery level needs continuous update
		if ((long)millis() - lastBatteryLevelUpdate > 100){
			// Serial.print("last Battery: ");
			// Serial.println(millis());
			lastBatteryLevelUpdate = millis();
			String bStr = "battery";
			SetExpression(bStr);
		}
	}
}
void SetExpression(String &label)
{
	if (!weHaveOled)
		return;
	showingBatteryLevel = false;
	label.toLowerCase();
	oled.clearDisplay();
	if (label == "confused")
		oled.drawBitmap(0, 0, epd_bitmap_confused, 128, 64, 1);
	else if (label == "frustrated")
		oled.drawBitmap(0, 0, epd_bitmap_frustrated, 128, 64, 1);
	else if (label == "funny")
		oled.drawBitmap(0, 0, epd_bitmap_funny, 128, 64, 1);
	else if (label == "joy")
		oled.drawBitmap(0, 0, epd_bitmap_joy, 128, 64, 1);
	else if (label == "laugh")
		oled.drawBitmap(0, 0, epd_bitmap_laugh, 128, 64, 1);
	else if (label == "like")
		oled.drawBitmap(0, 0, epd_bitmap_like, 128, 64, 1);
	else if (label == "love")
		oled.drawBitmap(0, 0, epd_bitmap_love, 128, 64, 1);
	else if (label == "smile")
		oled.drawBitmap(0, 0, epd_bitmap_smile, 128, 64, 1);
	else if (label == "wink")
		oled.drawBitmap(0, 0, epd_bitmap_wink, 128, 64, 1);
	else if (label == "battery")
	{
		showingBatteryLevel = true;
		float voltage = analogRead(BatteryLevelPin)  * (7.83 / 4.13F) /* Calib */ / 1023.0F /* analog read res */ * (10.0F / (2.2F + 10.0F)) /* Pot ratio*/ * 3.3F /* Vref */;
		float fraction = (voltage - 7.4F) / (8.2F - 7.4F);
		int index = round(fraction * 5);
		if (index < 0)
			index = 0;
		else if (index >= 6)
			index = 5;
		
		const unsigned char* bl_icon = battery_level_icons[index];
		oled.drawBitmap(0, 0, bl_icon, 128, 64, 1);
	}

	else
		oled.drawBitmap(0, 0, epd_bitmap_stuck, 128, 64, 1);
	oled.display();
}
void ShowMessage(String &msg)
{
	if (!weHaveOled)
		return;
	oled.clearDisplay();

	int16_t x, y;
	uint16_t w, h;
	int size = 1;
	for (int size = 1; size < 4; size++)
	{
		oled.setTextSize(size);
		oled.getTextBounds(msg, 0, 0, &x, &y, &w, &h);
		if (w >= 120 || h >= 60)
		{
			if (size > 1)
			{
				size--;
				oled.setTextSize(size);
				oled.getTextBounds(msg, 0, 0, &x, &y, &w, &h);
				break;
			}
		}
	}

	oled.setCursor(128 / 2 - w / 2, 64 / 2 - h / 2);
	oled.print(msg);
	oled.display();
}